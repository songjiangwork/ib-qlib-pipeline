@Injectable({ providedIn: 'root' })
export class FrontendI18nService {
  readonly language = signal<Language>('zh');

  constructor() {
    const saved = typeof localStorage !== 'undefined' ? localStorage.getItem('frontend-language') : null;
    if (saved === 'en' || saved === 'zh') {
      this.language.set(saved);
    }
  }

  setLanguage(language: Language): void {
    this.language.set(language);
    if (typeof localStorage !== 'undefined') {
      localStorage.setItem('frontend-language', language);
    }
  }

  t(key: string): string {
    return messages[this.language()][key] ?? key;
  }
}
